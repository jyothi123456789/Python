import numpy as np
zeroDarray = np.array([[4,5]])
print("Zero dimension array is : ",zeroDarray)
print("the dimension of the array is : ",zeroDarray.ndim)
print("The size of the array is : ",zeroDarray.size)
print("the shape of the array is: ",zeroDarray.shape)

